    //Tabelle füllen
     public void Tabelle_fuellen()
    {   

        Object[] row = new Object[4];
        DefaultTableModel model = (DefaultTableModel) jtuser.getModel();
        model.setRowCount(0);
        
        for (int i=0; i< alle.length; i++)
        {
            row[0]= alle[i][0];
            row[1]= alle[i][1];
            row[2]= alle[i][2];
            row[3]= alle[i][3];

            model.addRow(row);
        }
        
        //Anzahl der Zeilen anzeigen
        jlrow.setText(alle.length + " Rows returned");
        jcheckpass.setSelected(false);
        jlpassneu.setText("Passwort erneut eingeben:");
        jlpassneu.setForeground(Color.BLACK);
    }

