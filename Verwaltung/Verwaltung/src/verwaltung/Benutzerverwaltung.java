        try
        { 
            //in der schleife jede stelle durchgehen
        for (int i = 0; i < passwort2.length; i++)
        {
            //prüfen ob jede stelle gleich ist
            if (passwort1[i] == passwort2[i])
            {
                //prüfen ob es gleich lang ist
                if (passwort1.length == passwort2.length)
                {
                    ok=true;
                }
            }
        }
        }
        catch(Exception ex) //wenn die zweite eingabe länger ist die erste (Array outofBound Exception) ok wird zu false - speichern wird unmöglich
        {
            ok=false;
        }
        //bei true -> ausgabe grün
        if (ok)
        {
            jlpassneu.setText("übereinstimmung");
            jlpassneu.setForeground(Color.GREEN);
        }
        else
        {
            jlpassneu.setText("keine übereinstimmung");
            jlpassneu.setForeground(Color.RED); 
        } 
