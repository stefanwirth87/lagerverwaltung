    private void jbdeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbdeleteActionPerformed
        // Löschen Button
        //Message erstellen
        String result = "Möchten Sie den Benutzer "+jtuserid.getText()+", "+jtname.getText()+" wirklich löschen?";
        //Benutzer fragen
        int ok = JOptionPane.showConfirmDialog(null, result, "Löschen" , JOptionPane.YES_NO_OPTION);
            //ok==0 == ja
            if (ok == 0)
            {
                //Methode am Server aufrufen
               result=myServiceCaller.loescheBenutzer(jtuserid.getText());
               if (result.equals("Datensatz wurde gelöscht!"))
               {
                   //Meldung vom Server ausgeben
                    JOptionPane.showMessageDialog(null, result, "Erfolgreich", JOptionPane.INFORMATION_MESSAGE);
               }
               else
               {
                   //Meldung vom Server ausgeben
                    JOptionPane.showMessageDialog(null, result, "Fehler", JOptionPane.ERROR_MESSAGE);
               }  
               //Benutzer neu laden und anzeigen
               alle = myServiceCaller.AlleBenutzer();
               Tabelle_fuellen();
               //die Benutzerauswahl ist danach nicht ausgewählt
               jtuserid.setText("");
               jtname.setText("");
               jcrecht.setSelectedIndex(0);
               
               
            }
    }//GEN-LAST:event_jbdeleteActionPerformed

