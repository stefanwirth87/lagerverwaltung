    private void jtauftragaktMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtauftragaktMouseClicked
        // Klichen auf die Tabellen Spalte
        //angeklickte Zeile auslesen und abspeichern in klassenwiter variable
        int i = jtauftragakt.getSelectedRow();
        akt = i;
        //die angeklickte Zeile Blau hinterlegen
        jtauftragakt.setSelectionBackground(Color.blue);
        //Tabelle Modell holen
        TableModel model = jtauftragakt.getModel();
        //status herausziehen
        status = ((String) model.getValueAt(i, 2));
        //Textfelder / Combofelder bestücken
        jtbestellnr.setText((String) model.getValueAt(i, 1));
        kdnr = ((String) model.getValueAt(i, 3));
        nachname = ((String) model.getValueAt(i, 4));
        vorname = ((String) model.getValueAt(i, 5));
        String sum = ((String) model.getValueAt(i, 6));
        
        jtausg.setText("Kunde: "+kdnr+", "+nachname+", "+vorname+", "+sum+" Artikel" );
        //bei einem Doppelklick wird die Bestellung direkt geöffnet
        if (evt.getClickCount()==2) 
        { 
            if (jtbestellnr.getText().equals(""))
            {
                JOptionPane.showMessageDialog(null, "Bitte eine Bestellung wählen!", "Fehler", JOptionPane.ERROR_MESSAGE);
            }
            else
            {
                Bestellung B = new Bestellung(recht, name, jtbestellnr.getText(),kdnr, vorname, nachname, status);
                B.setExtendedState(java.awt.Frame.MAXIMIZED_BOTH);
                B.setVisible(true);
            }         
	}
    }//GEN-LAST:event_jtauftragaktMouseClicked

