    void createTable(int auswahl) 
    { 
        //Holen aller Datensätze
        alle = myServiceCaller.aktBestellungen(auswahl);

        //Tabelle füllen
        Object[] row = new Object[10];
        DefaultTableModel model = (DefaultTableModel) jtauftragakt.getModel();
        
        for (int i=0; i< alle.length; i++)
        {
            row[0]= alle[i][0];
            row[1]= alle[i][1];
            row[2]= alle[i][2];
            row[3]= alle[i][3];
            row[4]= alle[i][4];
            row[5]= alle[i][5];
            row[6]= alle[i][6];

            model.addRow(row);
        }
        
        //Anzahl der Zeilen anzeigen
        jlrows.setText(alle.length + " Rows returned");
    }
    
    //Methode, wenn eine suche ausgeführt wird das die Tabelle mit den Daten 
    //gefüllt wird
    void fuellemitsuche() //ähnlich wie bei createtable()
    {
    if (alle.length > 0)
        {
            //Tabelle füllen
            Object[] row = new Object[10];
            DefaultTableModel model = (DefaultTableModel) jtauftragakt.getModel();
            model.setRowCount(0);

            for (int i=0; i< alle.length; i++)
            {
                row[0]= alle[i][0];
                row[1]= alle[i][1];
                row[2]= alle[i][2];
                row[3]= alle[i][3];
                row[4]= alle[i][4];
                row[5]= alle[i][5];
                row[6]= alle[i][6];

                model.addRow(row);
            }

            //Anzahl der Zeilen anzeigen
            jlrows.setText(alle.length + " Rows returned");
        }
    else //wenn nichts gefunden wird
        {
               JOptionPane.showMessageDialog(null, "Ihre Suche hat nichts ergeben!", "Fehler", JOptionPane.ERROR_MESSAGE);
        }
    }

