    private void jbsuchenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbsuchenActionPerformed
        // suchen
        //anlegen von Variablen
        String Datevon="", Datebis="", Suchbegriff="";
         //Simple date für die Formatierung wählen
         SimpleDateFormat s = new SimpleDateFormat("yyyy-MM-dd");
         Date d = new Date();
        //überprüfen ob der kalender gesetzt ist (!=null)
        if ((jdcvon.getCalendar() != null)&&(jdcbis.getCalendar() != null))
        {
            //wandlung der Millisekunden in Zeitformate 
            //(Datebis wird noch um einen Tag hochgezählt) 
            //-> bei gleichem Datum wäre kein beetween im SQL Befehl mehr möglich
            Datevon = s.format(jdcvon.getCalendar().getTimeInMillis());
            Datebis = s.format(jdcbis.getCalendar().getTimeInMillis()+10000000);
            System.out.println(Datebis);
            //Suchbegriff abspeichern
            Suchbegriff = jtsuche.getText();
            //Suche ausführen und Daten holen
            alle = myServiceCaller.sucheBestellung(Datevon, Datebis, Suchbegriff);
            //Methode ausführen
            fuellemitsuche();
        }//Falls kein Datum ausgeählt wurd (==null)
        else if ((jdcvon.getCalendar() == null)&&(jdcbis.getCalendar() == null))
        {
            //bei keiner auswahl zur suche den gesamten Programmzeitraum angeben
            Datevon = "2016-01-01";
            Datebis = s.format(d);
            //Suchbegriff abspeichern
            Suchbegriff = jtsuche.getText();
            //Suche ausführen und Daten holen
            alle = myServiceCaller.sucheBestellung(Datevon, Datebis, Suchbegriff);  
            //fuellen methode ausführen
            fuellemitsuche();
        }    
        else
        {
            JOptionPane.showMessageDialog(null, "Bitte einen Zeitraum angeben!", "Fehler", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbsuchenActionPerformed

